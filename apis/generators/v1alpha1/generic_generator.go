package v1alpha1

import (
	apiextensions "k8s.io/apiextensions-apiserver/pkg/apis/apiextensions/v1"
	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
	"k8s.io/apimachinery/pkg/runtime"
)

// +kubebuilder:object:root=false
// +kubebuilder:object:generate:false
// +k8s:deepcopy-gen:interfaces=nil
// +k8s:deepcopy-gen=nil

// GenericGenerator is a common interface for interacting with Generators.
type GenericGenerator interface {
	runtime.Object
	metav1.Object

	GetObjectMeta() *metav1.ObjectMeta
	GetTypeMeta() *metav1.TypeMeta
	GetKind() string

	SetOutputs(expectedOutput map[string]string) error
	Copy() GenericGenerator
}

// GeneratorStatus represents the status of a generator.
type GeneratorStatus struct {
	Output *apiextensions.JSON `json:"output,omitempty"`
}
