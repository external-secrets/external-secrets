#!/usr/bin/env bash

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
PROVIDERS_DIR="${PROJECT_ROOT}/providers/v1"
OUTPUT_FILE="${PROJECT_ROOT}/pkg/providers/imports/generated_imports.go"

# Ensure output directory exists
mkdir -p "$(dirname "${OUTPUT_FILE}")"

# Discover all providers (directories with provider.go)
PROVIDERS=()
for provider_dir in "${PROVIDERS_DIR}"/*; do
    if [[ -d "${provider_dir}" && -f "${provider_dir}/provider.go" ]]; then
        provider_name=$(basename "${provider_dir}")
        PROVIDERS+=("${provider_name}")
    fi
done

# Sort providers for deterministic output
IFS=$'\n' PROVIDERS=($(sort <<<"${PROVIDERS[*]}"))
unset IFS

# Generate header
cat > "${OUTPUT_FILE}" << 'EOF'
// Code generated by hack/generate-provider-imports.sh. DO NOT EDIT.

/*
