WITHOUT WARRANTIES OR CONDITIONS OF ANY KIND, either express or implied.
See the License for the specific language governing permissions and
limitations under the License.
*/

// Package imports provides automatic importing of all provider packages
// for registration via their init() functions.
package imports

import (
EOF

# Generate imports for each provider
for provider in "${PROVIDERS[@]}"; do
    echo "	_ \"github.com/external-secrets/external-secrets/providers/v1/${provider}\" // ${provider} provider registration" >> "${OUTPUT_FILE}"
done

# Close the import block
echo ")" >> "${OUTPUT_FILE}"

echo "Generated provider imports for ${#PROVIDERS[@]} providers: ${PROVIDERS[*]}"
